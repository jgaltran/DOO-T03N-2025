package Fag;

public class Venda {
	String nome;
	int unid;
	float preco;
	float desc;
	float precoTotal;
	public Venda(String nome, int unid, float preco, float desc, float precoTotal) {
		this.nome = nome;
		this.unid = unid;
		this.preco = preco;
		this.desc = desc;
		this.precoTotal = precoTotal;
	}
}
